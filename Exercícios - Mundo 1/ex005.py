# Faça um programa que leia um número inteiro e mostre na tela o seu sucessor e antecessor

# Variável resgatando os números via input()
num = float(input("Digite um número qualquer: "))

# Mostrando o sucessor e antecessor via print()
print("O número {} tem seu".format(num))
print("Sucessor: ", num + 1)
print("Antecessor: ", num - 1)
