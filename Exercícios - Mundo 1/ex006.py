# Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada

# Variável resgatando o número via input()
num = float(input("Digite um número qualquer: "))

# Mostrando o dobro, triplo e a raiz via print()
print("O número {} tem seu".format(num))
print("Dobro: ", num * 2)
print("Triplo: ", num * 3)
print("Raiz: ", num ** (1/2))
