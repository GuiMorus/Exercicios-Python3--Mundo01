# Desenvolva um programa que leia as duas notas de um aluno, calcule e mostre a sua média

# Resgatando as notas via input()
num1 = float(input("Digite a 1º nota: "))
num2 = float(input("Digite a 2º nota: "))

# Mostrando a média das notas via print()
print("Com as notas {} e {} tem sua".format(num1, num2))
print("Média :", (num1 + num2) / 2)
